    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
         CalculosVenta valor = new CalculosVenta(); 
         String idcredito;  
         String numerocuotas;
         float valorcredito;
         float valorcuota;
         int fila = tablalistado.getSelectedRow();
         idcredito=tablalistado.getValueAt(fila,0).toString();
         numerocuotas =  tablalistado.getValueAt(fila,6).toString();
         valorcredito = Float.parseFloat(tablalistado.getValueAt(fila,7).toString());
         valorcuota = valor.valorCuota(valorcredito, Integer.parseInt(numerocuotas));
         frmCuota.txtidcredito.setText(idcredito);
         frmCuota.txtvalorcuota.setText(Float.toString(valorcuota));
         
         this.dispose();
    
    }//GEN-LAST:event_tablalistadoMouseClicked

